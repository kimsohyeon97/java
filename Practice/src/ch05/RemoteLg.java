package ch05;

public class RemoteLg implements RemoteControl{

	@Override
	public void powerOn() {
		System.out.println("파워온");
		
	}

	@Override
	public void powerOff() {
		System.out.println("파워온");
		
	}

	@Override
	public void chUp() {
		System.out.println("파워온");
		
	}

	@Override
	public void chDown() {
		System.out.println("파워온");
		
	}

	@Override
	public void soundUp() {
		System.out.println("파워온");
		
	}

	@Override
	public void soundDown() {
		System.out.println("파워온");
		
	}

}
